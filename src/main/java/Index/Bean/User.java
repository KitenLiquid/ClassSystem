package Index.Bean;

public class User {
	String name;
	String password;
	String index;
	int age;
	String si;
	String eg;
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setPassword(String password) {
		this.password=password;
	}
	public String getPassword() {
		return password;
	}
	public void setIndex(String index) {
		this.index=index;
	}
	public String getIndex() {
		return index;
	}
	public void setAge(int age) {
		this.age=age;
	}
	public int getAge() {
		return age;
	}
	public void setSi(String si) {
		this.si=si;
	}
	public String getSi() {
		return si;
	}
	public void setEg(String eg) {
		this.eg=eg;
	}
	public String getEg() {
		return eg;
	}
}
