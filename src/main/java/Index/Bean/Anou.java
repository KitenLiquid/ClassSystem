package Index.Bean;

public class Anou {
	String name;
	String content;
	int No;
	public void setName(String name) {
		this.name=name;
	}
	public void setContent(String content) {
		this.content=content;
	}
	public String getName() {
		return name;
	}
	public String getContent() {
		return content;
	}
	public void setNo(int No) {
		this.No=No;
	}
	public int getNo() {
		return No;
	}
}
